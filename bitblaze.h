#define KEY_AUTO_LOOP_CLOSE ')'
#define KEY_FUNCTION_OPEN '{'
#define KEY_FUNCTION_CLOSE '}'
#define KEY_CLOSE ';'
#define KEY_PRINT '#'
#define KEY_INPUT '?'
#define KEY_RUN '*'
#define KEY_STRING '"'

#endif